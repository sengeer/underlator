/**
 * @module ChunkTextManagerTypes
 * Типы для ChunkTextManager.
 * Определяет алгебраические типы для обработки результатов операций.
 */

/**
 * Алгебраический тип для результатов операций с текстовыми фрагментами.
 *
 * Обеспечивает типобезопасную обработку успешных и ошибочных результатов операций.
 * Реализует паттерн Result/Either для явной обработки ошибок.
 *
 * @template T - Тип данных, возвращаемых при успешном выполнении операции.
 */
export type ChunkOperationResult<T> =
  /** Успешное выполнение операции */
  | { success: true; data: T }
  /** Неуспешное выполнение операции */
  | { success: false; error: string };
