/**
 * @module ThemesSliceTypes
 * Типы для ThemesSlice.
 * Определяет интерфейсы для управления темами приложения.
 */

/**
 * Цветовая схема темы.
 * Соответствует структуре CSS переменных приложения.
 */
export interface ThemeColors {
  /** Основной цвет интерфейса */
  main: string;
  /** Цвет фона */
  background: string;
  /** Акцентный цвет */
  accent: string;
  /** Цвет переднего плана (текст) */
  foreground: string;
}

/**
 * Тема приложения.
 * Содержит название темы и её цветовую схему.
 */
export interface Theme {
  /** Название темы (например, "dracula", "sand") */
  name: string;
  /** Цветовая схема темы */
  colors: ThemeColors;
}

/**
 * Состояние управления темами.
 * Содержит список доступных тем и выбранную активную тему.
 * Поддерживает как предустановленные, так и пользовательские темы.
 */
export interface ThemesState {
  /** Массив всех доступных тем (предустановленные + пользовательские) */
  themes: Theme[];
  /** Название активной темы */
  activeTheme: string;
}
