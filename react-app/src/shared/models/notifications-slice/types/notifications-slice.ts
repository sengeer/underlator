/**
 * @module NotificationsSliceTypes
 * Типы для NotificationsSlice.
 * Определяет интерфейсы и структуры данных для системы toast-уведомлений.
 */

/**
 * Тип toast-уведомления.
 * Определяет возможные варианты отображения уведомлений в интерфейсе.
 */
export type NotificationType =
  /** Информационное уведомление */
  | 'info'
  /** Успешное выполнение операции */
  | 'success'
  /** Ошибка выполнения операции */
  | 'error';

/**
 * Сообщение уведомления.
 * Может быть простой строкой или объектом для сложных случаев.
 */
export type NotificationMessage =
  /** Простое текстовое сообщение */
  | string
  /** Сложное сообщение с дополнительными данными */
  | Record<string, any>;

/**
 * Создатель уведомления без идентификатора.
 * Используется для добавления нового уведомления в store.
 */
export interface NotificationPayload {
  /** Тип уведомления, определяющий стиль отображения */
  type: NotificationType;
  /** Содержимое сообщения уведомления */
  message: NotificationMessage;
}

/**
 * Полное уведомление с идентификатором.
 * Содержит всю информацию об уведомлении, включая уникальный ID.
 */
export interface Notification extends NotificationPayload {
  /** Уникальный идентификатор уведомления */
  id: string;
}

/**
 * Селектор состояния уведомлений.
 * Используется для удобного доступа к уведомлениям из компонентов.
 */
export interface NotificationsState {
  /** Массив активных уведомлений */
  notifications: Notification[];
}
