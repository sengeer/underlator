/**
 * @module SwitchTypes
 * Типы для компонента Switch, используемого как бинарный переключатель режимов.
 */

/**
 * Пропсы для компонента Switch.
 *
 * Компонент применяется поверх глобальных настроек интерфейса,
 * например, в `pdf-viewer` управляет переключением между переводом и инструкциями.
 */
export interface SwitchProps {
  /** Отражает активный режим (true — включен альтернативный сценарий) */
  checked: boolean;
  /** Колбэк для смены режима */
  onChange: () => void;
}
