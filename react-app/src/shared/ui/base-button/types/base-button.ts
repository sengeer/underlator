/**
 * @module BaseButtonTypes
 * Типы для базового компонента кнопки.
 */

/**
 * Пропсы базовой кнопки, используемой как основа для специализированных кнопок.
 */
export interface BaseButtonProps {
  /** Дополнительный контент (например, иконка), отображаемый слева от текста */
  children?: React.ReactNode;
  /** Пользовательские CSS классы для модификации внешнего вида */
  className?: string;
  /** Идентификатор DOM-узла для интеграции с тестами или якорями */
  id?: string;
  /** Текстовое содержимое кнопки */
  text?: string;
  /** Инлайн-стили, применяемые к корневому элементу кнопки */
  style?: React.CSSProperties;
  /** Обработчик клика по кнопке */
  onClick?: () => void;
  /** Включает активное состояние (например, подсветку выбранного фильтра) */
  isActiveStyle?: boolean;
  /** Блокирует кнопку и предотвращает обработку действия */
  isDisabled?: boolean;
  /** Текст всплывающей подсказки (`title` атрибут) */
  tooltipText?: string;
  /** Обработчик наведения на кнопку */
  onMouseEnter?: () => void;
  /** Обработчик ухода курсора с кнопки */
  onMouseLeave?: () => void;
}
